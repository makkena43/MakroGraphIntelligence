@dataclass
class BusinessEvent:
    """A discrete business event extracted from a filing or transcript.

    Events are the atomic unit of the event-centric architecture.
    Markets react to events, not raw document text.
    """
    event_type: EventType
    subject_entity: str             # company / technology / commodity being affected
    subject_type: NodeType = NodeType.COMPANY
    description: str = ""
    magnitude: Optional[float] = None      # quantified size if present
    magnitude_unit: str = ""               # USD_bn, pct, units
    direction: str = "positive"            # positive | negative | neutral
    confidence: float = 0.75
    document_id: Optional[int] = None
    filed_at: Optional[date] = None
    context_text: str = ""
    second_order_entities: list[str] = field(default_factory=list)  # who else is affected
    metadata: dict = field(default_factory=dict)

    def __post_init__(self):
        if self.filed_at is None:
            self.filed_at = date.today()


