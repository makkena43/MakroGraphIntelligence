"""Core ontology data models: nodes, edges, relationships, and graph events."""

from dataclasses import dataclass, field
from datetime import date
from enum import Enum
from typing import Optional


class NodeType(str, Enum):
    COMPANY = "Company"
    TECHNOLOGY = "Technology"
    SECTOR = "Sector"
    CONCEPT = "Concept"
    PRODUCT = "Product"
    PERSON = "Person"
    REGULATION = "Regulation"
    LOCATION = "Location"
    THEME = "Theme"
    EVENT = "Event"                 # Business events: factory expansion, shortage, etc.


class RelationType(str, Enum):
    DEVELOPS = "DEVELOPS"
    INVESTS_IN = "INVESTS_IN"
    USES = "USES"
    COMPETES_WITH = "COMPETES_WITH"
    SUPPLIES_TO = "SUPPLIES_TO"
    REGULATED_BY = "REGULATED_BY"
    MENTIONED_IN = "MENTIONED_IN"
    PART_OF = "PART_OF"
    LEADS = "LEADS"
    ACQUIRES = "ACQUIRES"
    ENABLES = "ENABLES"
    DISRUPTS = "DISRUPTS"
    # --- Strategic / Causal ---
    DEPENDS_ON = "DEPENDS_ON"       # upstream dependency (supply chain)
    CONSTRAINS = "CONSTRAINS"       # bottleneck / regulatory constraint
    SUPPLIES = "SUPPLIES"           # direct material/component supplier
    BENEFITS_FROM = "BENEFITS_FROM" # second-order thematic beneficiary
    REPLACES = "REPLACES"           # displacement / substitution
    ENABLED_BY = "ENABLED_BY"       # requires an upstream enabler
    EXPOSED_TO = "EXPOSED_TO"       # risk exposure (geopolitical, regulatory)


class SignalDirection(str, Enum):
    POSITIVE = "positive"
    NEGATIVE = "negative"
    NEUTRAL = "neutral"


class ThemeConviction(str, Enum):
    EMERGING = "emerging"
    DEVELOPING = "developing"
    CONFIRMED = "confirmed"
    DECLINING = "declining"


@dataclass
class OntologyNode:
    """A node in the knowledge graph."""
    name: str
    node_type: NodeType
    properties: dict = field(default_factory=dict)
    mention_count: int = 1
    first_seen_at: Optional[date] = None
    last_seen_at: Optional[date] = None
    importance_score: float = 0.0
    neo4j_id: Optional[str] = None

    def __post_init__(self):
        if self.first_seen_at is None:
            self.first_seen_at = date.today()
        if self.last_seen_at is None:
            self.last_seen_at = date.today()

    @property
    def key(self) -> tuple:
        return (self.name.lower().strip(), self.node_type)


@dataclass
class OntologyEdge:
    """A directed relationship in the knowledge graph."""
    source_name: str
    source_type: NodeType
    relation: RelationType
    target_name: str
    target_type: NodeType
    weight: float = 1.0
    properties: dict = field(default_factory=dict)
    first_seen_at: Optional[date] = None
    last_seen_at: Optional[date] = None
    neo4j_rel_id: Optional[str] = None

    def __post_init__(self):
        if self.first_seen_at is None:
            self.first_seen_at = date.today()
        if self.last_seen_at is None:
            self.last_seen_at = date.today()

    @property
    def key(self) -> tuple:
        return (self.source_name.lower(), self.relation, self.target_name.lower())


@dataclass
class GraphEvent:
    """An ontology change event (for temporal tracking)."""
    event_type: str             # node_added | edge_added | weight_increased | node_removed
    entity_name: str
    entity_type: NodeType
    relation: Optional[RelationType] = None
    target_name: Optional[str] = None
    delta: float = 0.0
    event_date: Optional[date] = None
    document_id: Optional[int] = None
    metadata: dict = field(default_factory=dict)

    def __post_init__(self):
        if self.event_date is None:
            self.event_date = date.today()


@dataclass
class InvestmentTheme:
    """A detected cross-sector investment theme.

    Canonicalization fields (set by ThemeCanonicalizer after detection):
        is_canonical:       True  → this is a top-level parent theme shown in the UI.
                            False → this is a subtheme; rolled up under parent_theme_slug.
        canonical_name:     Clean human-readable name generated by the merge engine
                            (e.g. "AI Infrastructure Power Constraint").
        aliases:            Alternative names for the same macro-theme (canonical only).
        parent_theme_slug:  slug of the canonical parent; None when is_canonical=True.
    """
    theme_name: str
    theme_slug: str
    description: str = ""
    sectors: list[str] = field(default_factory=list)
    signal_types: list[str] = field(default_factory=list)
    strength_score: float = 0.0
    momentum_score: float = 0.0
    conviction: ThemeConviction = ThemeConviction.EMERGING
    first_detected: Optional[date] = None
    last_updated: Optional[date] = None
    doc_count: int = 0
    company_count: int = 0
    hypothesis_text: str = ""
    metadata: dict = field(default_factory=dict)
    # ── Canonicalization fields ───────────────────────────────────────────────
    is_canonical: bool = True
    canonical_name: str = ""
    aliases: list[str] = field(default_factory=list)
    parent_theme_slug: Optional[str] = None
    country: str = "US"

    def __post_init__(self):
        # NOTE: first_detected intentionally left as None when not yet confirmed.
        # Defaulting to date.today() caused future leakage in replay mode and
        # mass-clustering of themes to the same wall-clock date.
        # It will be set only after multi-quarter evidence crosses threshold.
        if self.last_updated is None:
            self.last_updated = date.today()
        if not self.theme_slug:
            self.theme_slug = self.theme_name.lower().replace(" ", "-").replace("/", "-")
        if not self.canonical_name:
            self.canonical_name = self.theme_name

    def to_dict(self) -> dict:
        return {
            "theme_name":        self.theme_name,
            "theme_slug":        self.theme_slug,
            "description":       self.description,
            "sectors":           self.sectors,
            "signal_types":      self.signal_types,
            "strength_score":    self.strength_score,
            "momentum_score":    self.momentum_score,
            "conviction":        self.conviction.value,
            "first_detected":    str(self.first_detected) if self.first_detected else None,
            "last_updated":      str(self.last_updated),
            "doc_count":         self.doc_count,
            "company_count":     self.company_count,
            "hypothesis_text":   self.hypothesis_text,
            "metadata":          self.metadata,
            # Canonicalization
            "is_canonical":      self.is_canonical,
            "canonical_name":    self.canonical_name,
            "aliases":           self.aliases,
            "parent_theme_slug": self.parent_theme_slug,
            "country":           self.country,
        }


@dataclass
class ThemeBeneficiary:
    """A company or entity that benefits from an investment theme."""
    theme_slug: str
    entity_name: str
    ticker: Optional[str] = None
    company_name: str = ""
    beneficiary_type: str = "direct"     # direct | indirect | disruptee
    company_role: str = ""               # infrastructure_provider | supplier | bottleneck_player | beneficiary | downstream_user | hidden_enabler
    relevance_score: float = 0.0
    signal_count: int = 0
    first_seen_at: Optional[date] = None
    last_seen_at: Optional[date] = None
    rank_in_theme: Optional[int] = None
    reasoning: str = ""

    def __post_init__(self):
        if not self.company_name:
            self.company_name = self.entity_name
        if self.first_seen_at is None:
            self.first_seen_at = date.today()
        if self.last_seen_at is None:
            self.last_seen_at = date.today()


