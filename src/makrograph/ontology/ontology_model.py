@dataclass
class NarrativePropagation:
    """Tracks how a narrative spreads across companies and sectors over time.

    A narrative is confirmed when the same theme is independently mentioned
    by companies across different sectors within a rolling time window.
    """
    narrative_slug: str
    narrative_name: str
    origin_company: str
    origin_date: Optional[date] = None
    propagation_trail: list[dict] = field(default_factory=list)
    # Each entry: {"company": str, "sector": str, "date": str, "signal_type": str}
    sector_spread: list[str] = field(default_factory=list)
    velocity: float = 0.0           # mentions per 30-day window
    acceleration: float = 0.0       # change in velocity vs prior window
    diffusion_score: float = 0.0    # 0-100 breadth of spread
    is_confirmed: bool = False

    def __post_init__(self):
        if self.origin_date is None:
            self.origin_date = date.today()

    def add_propagation(self, company: str, sector: str, event_date: date, signal_type: str):
        self.propagation_trail.append({
            "company": company,
            "sector": sector,
            "date": str(event_date),
            "signal_type": signal_type,
        })
        if sector not in self.sector_spread:
            self.sector_spread.append(sector)
        if len(self.sector_spread) >= 3:
            self.is_confirmed = True
