@dataclass
class CausalLink:
    """A single step in a causal chain."""
    cause: str              # entity / concept name
    cause_type: NodeType
    effect: str             # entity / concept name
    effect_type: NodeType
    mechanism: str = ""     # short description of how cause → effect
    probability: float = 0.7
    lag_days: int = 90      # typical lag between cause and effect manifesting
    relation: RelationType = RelationType.ENABLES


@dataclass
class CausalChain:
    """A multi-hop causal chain between themes and entities.

    Example:
        AI Datacenter Buildout
          → ENABLES → Power Demand Surge (lag 90d)
          → ENABLES → Transformer Shortage (lag 180d)
          → ENABLES → Copper Demand Increase (lag 270d)
    """
    chain_id: str
    name: str
    description: str = ""
    links: list[CausalLink] = field(default_factory=list)
    activation_score: float = 0.0   # how strongly this chain is firing right now
    first_detected: Optional[date] = None

    def __post_init__(self):
        if self.first_detected is None:
            self.first_detected = date.today()

    @property
    def depth(self) -> int:
        return len(self.links)

    @property
    def terminal_effect(self) -> Optional[str]:
        return self.links[-1].effect if self.links else None

    def activation_summary(self) -> dict:
        return {
            "chain_id": self.chain_id,
            "name": self.name,
            "depth": self.depth,
            "terminal_effect": self.terminal_effect,
            "activation_score": self.activation_score,
        }


